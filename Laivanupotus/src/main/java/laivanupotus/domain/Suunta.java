package laivanupotus.domain;

/**
 * Suunnat, joita tekoalyn LaivojenAsettaja käyttää apuna asettaessaan laivoja
 * pelilaudalle tietystä ruudusta alkaen
 */
public enum Suunta {

    /**
     * Suunta pelilaudalla ylöspäin
     */
    YLOS,

    /**
     * Suunta pelilaudalla alaspäin
     */
    ALAS,

    /**
     * Suunta pelilaudalla oikealle
     */
    OIKEA,

    /**
     * Suunta pelilaudalle vasemmalla
     */
    VASEN
}
